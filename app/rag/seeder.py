def generate_seed_records(count: int = 260) -> list[SeedRecord]:
    records: list[SeedRecord] = []
    for category, policies in BUSINESS_POLICIES.items():
        if category in {"奖助管理", "信息化服务", "实验室安全", "财务宿舍"}:
            for major in MAJORS:
                for policy in policies:
                    records.append(_major_record(category, major, policy))
        else:
            for policy in policies:
                records.append(_record(category, policy))
    records.extend(_program_records())
    records.extend(_calendar_records())
    records.extend(_office_records())
    records.extend(COURSE_PROJECT_RECORDS)
    records.extend(_cross_process_records())
    return records[:count]


def write_seed_markdown(output_path: Path, count: int = 260) -> Path:
    output_path.parent.mkdir(parents=True, exist_ok=True)
    records = generate_seed_records(count)
    lines = ["# 研究生教务与校园服务知识库", ""]
    lines.append("本文件为 EduRAG-Agent 演示用模拟企业级知识库。它不是一问一答，而是结构化业务文档片段，覆盖研究生从入学、培养、学位到离校的常见流程。")
    lines.append("")
    for index, record in enumerate(records, start=1):
        lines.append(f"## KB-{index:04d} {record.category}：{record.title}")
        lines.append("")
        lines.append(record.body)
        lines.append("")
    output_path.write_text("\n".join(lines), encoding="utf-8")
    return output_path


def _record(category: str, policy: tuple[str, str, str, str, str]) -> SeedRecord:
    name, scenario, materials, entrance, risk = policy
    return SeedRecord(
        category=category,
        title=name,
        body=f"业务场景：{scenario}。办理材料：{materials}。办理入口：{entrance}。审核流程通常为学生提交、导师确认、学院审核，必要时提交研究生院复核。注意事项：{risk}。",
    )


def _major_record(category: str, major: str, policy: tuple[str, str, str, str, str]) -> SeedRecord:
    base = _record(category, policy)
    return SeedRecord(category=category, title=f"{major} {base.title}", body=f"适用对象：{major}专业研究生。{base.body}")


